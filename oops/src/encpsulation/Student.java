package encpsulation;

public class Student {
	
	private String name;
	private int age;
	private int marks;
	
	public void setName(String name) {
		this.name= name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	public int getAge() {
		return age;
	}
	public void setMark(int mark) {
		this.marks = mark;
	}
	public int getMark() {
		return marks;
	}

}
