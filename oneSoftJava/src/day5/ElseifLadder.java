package day5;

public class ElseifLadder {
	
	public static void main(String[] args) {
		
		int a = 5;
		
		if(a>0) {
			System.out.println("is positive number");
			
		}
		else if(a<0) {
			System.out.println("is negative number");
		}
		else if(a==0) {
			System.out.println("is zero");
		}
		else {
			System.out.println("its not a number");
		}
	}

}
